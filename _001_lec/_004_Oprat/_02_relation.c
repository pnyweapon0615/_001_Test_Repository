#include "stdio.h"

void main() {
	int vall = 10, val2 = 20, result = 0;
	printf("관계연산자 : \n");

	result = vall > val2;
	printf("%d\n", result);
	result = vall < val2;
	printf("%d\n", result);
	result = vall == val2;
	printf("%d\n", result);
	result = vall >= val2;
	printf("%d\n", result);
	result = vall <= val2;
	printf("%d\n", result);
}
